    <h3 class="text-white">From Westside to Westlife</h3>

    <p class="text-justify">The group were obliged to change their name from Westside to Westlife early in 1999 when it was discovered that a
        number of existing bands shared the same name. Undeterred, Westlife went on to release their first single Swear it
        Again in April 1999 which went straight to the top of the British and Irish charts, becoming the biggest-selling
        debut single ever in Ireland.</p>

    <p class="text-justify">Westlife’s eagerly-awaited debut album Westlife was released in the UK in November 1999, with double A-side
        single I Have a Dream and Seasons in the Sun becoming the UK Christmas number one of 1999. Westlife won Record
        of the Year for Flying Without Wings in 1999 and My Love in the following year.</p>

    <p class="text-justify">Winning numerous awards left right and centre, Westlife achieved much acclaim and enjoyed a lot of success,
        breaking records as fast as they set them.  Their second album, Coast To Coast went triple platinum in Ireland
        after only one day of release in 2000, and their seventh single release, My Love, gave them a place in chart
        history as they became the only band in UK chart history to have their first seven singles debut at number one.</p>

    <p class="text-justify">2002 saw the group release their first Greatest Hits album, and Westlife’s subsequent album, Turnaround, released
        November 2003, continued the group’s success, going straight into the charts at number one.</p>

    <h3 class="text-white">From five to four</h3>

    <p class="text-justify">March 2004 saw a new chapter begin for the group as Brian McFadden announced his departure from the band, citing
        his desire to spend more time with his young family. Rumours suggested that this might be the beginning of the
        end for the group proved unfounded, as Westlife continued as a four-piece, “in the tradition of all great pop
        acts”.  </p>

    <p class="text-justify">2004 saw the release of their fifth album Allow us to be Frank, covers of Frank Sinatra hits, followed by Face
        to Face in 2005 and then The Love Album, a concept album featuring covers of popular love songs, in 2006.</p>

    <p class="text-justify">By 2007 Westlife had achieved a massive fourteen number ones in the UK – the third-highest such total in UK
        history, behind Elvis Presley and The Beatles, and tied with Cliff Richard.  Their ninth album, Back Home,
        also debuted at number one on its release in 2007.</p>

    <p class="text-justify">Following a world tour in 2008, Westlife took a well-deserved one-year break, followed by their tenth album,
        Where We Are, released in November 2009, and Gravity, their eleventh album, in 2010.</p>

    <h3 class="text-white">Greatest Hits and the split</h3>

    <p class="text-justify">On 19 October 2011, Westlife officially announced they were splitting after a Greatest Hits album and a farewell tour in 2012.
        The final performance of the tour was at Croke Park Stadium in Dublin on 23 June 2012, to an audience of 82,300, and streamed
        live to 200 cinemas worldwide.</p>

